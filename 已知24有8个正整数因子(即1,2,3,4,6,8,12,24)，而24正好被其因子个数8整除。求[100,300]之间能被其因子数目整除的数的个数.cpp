//程序问题：已知24有8个正整数因子(即:1,2,3,4,6,8,12,24)，而24正好被其因子个数8整除。求[100,300]之间能被其因子数目整除的数的个数
 
#include<stdio.h>
int main()
{
	int i,t,n,s=0;
	for(i=100;i<=300;i++)
	{
		n=0;
		for(t=1;t<=i;t++)
		if(i%t==0)
		n++;
		if(i%n==0)
		{
			s++;
			printf("i=%d,s=%d\n",i,s);
		}
	}
}
