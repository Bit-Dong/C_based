//程序问题：求满足上述条件的所有真分数个数
 
#include<stdio.h>            //真分数：如果将该分数的分子个位数和分母十位数同时去掉，所得结果正好等于原分数约分后的结果
int main()                   // 例如16/64=1/4 
{                            //分母要大于分子 
	int i,j,n=0,a,b;
	for(i=10;i<=99;i++)
	{
		a=i/10;
		for(j=i+1;j<=99;j++)
		{
			b=j%10;
			if(a*j==b*i)
			{
				n++;
				printf("i=%d,j=%d,a=%d,b=%d,n=%d\n",i,j,a,b,n);
			}
		}
	}
}
