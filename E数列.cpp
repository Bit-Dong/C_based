//程序问题：求[1, 30000]之内E数的个数，求不超过30000的最大E数的值。 

#include<stdio.h>               //E数列：数列E(1)=E(2)=1   E(n)=(n-1)*E(n-1)+(n-2)*E(n-2)    (n>2) 每一个E(n),(n=1,2,…)称为E数。 
int main()
{
	double a=1,b=1,c,n,i=2;
	for(n=3;n<=120000;n++)
	{
		c=(n-1)*b+(n-2)*a;
		if(c<=30000)
		{
		i++;
		printf("i=%f,c=%f\n",i,c);
	    }
		a=b;
		b=c;
	}
 } 
