//程序问题：一张一元钞票,换成一分、二分和五分硬币,每种至少1枚,问兑换后硬币总数为50枚的兑换方案有多少种
 
#include<stdio.h>
int main()
{
	int a,b,c,n=0;
	for(a=1;a<=100;a++)
	{
		for(b=1;b<=50;b++)
		{
			for(c=1;c<=20;c++) 
			{
				if(a+2*b+5*c==100&&a+b+c==50)
				{
					n++;
					printf("a=%d,b=%d,c=%d,n=%d\n",a,b,c,n);
				}
			}
		}
	}
}
