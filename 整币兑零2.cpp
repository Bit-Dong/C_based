//程序问题：把一张一元钞票,换成一分、二分和五分硬币,每种至少1枚,问兑换后其中二分的硬币恰有20枚的方案共有多少种? 
 
#include<stdio.h>
int main()
{
	int a,b,c,n=0;
	for(a=1;a<=100;a++)
	{
		for(c=1;c<=20;c++)
		{
			b=20;
			if(a+2*b+5*c==100)
			{
				n++;
				printf("a=%d,b=%d,c=%d,n=%d\n",a,b,c,n);
			}
		}
	}
}
