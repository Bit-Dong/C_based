//程序问题：用100钱买100只鸡，公鸡一只五钱，母鸡一只三钱，小鸡三只一钱，编程计算共有几种买法 （要求每种鸡至少要买1只）？
 
#include<stdio.h>
int main()
{
	int a,b,c,n=0;
	for(a=1;a<=20;a++)
	{
		for(b=1;b<=33;b++)
		{
			for(c=1;c<=300;c++)
			{
				if(15*a+9*b+c==300&&a+b+c==100)
				{
					n++;
					printf("a=%d,b=%d,c=%d,n=%d\n",a,b,c,n);
				}
			}
		}
	}
}
